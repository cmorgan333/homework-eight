// this is a click listener
function init() {
  $("nav a").click(function (event) {
    let btnId = this.id;
    let contentID = btnId + "Content";

    $("#app").html(eval(contentID));
  });
}

// this checks for all the elements to be read by the browser
$(document).ready(function () {
  init();
});
